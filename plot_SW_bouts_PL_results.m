function plot_SW_bouts_PL_results(varargin)
%% Input arguments
exps = varargin{1};
if length(varargin) >1
    include_mean_pl_results = varargin{2};
else
    include_mean_pl_results = 1;
end

%% Model
if include_mean_pl_results
    for i=1:length(exps)
        figure,
        temp = exps(i).bouts.wake.no_trans;
        subplot(2,3,1);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (no trans.)', ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.wake.prev_trans;
        subplot(2,3,2);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (prev trans.)', ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.wake.half_trans;
        subplot(2,3,3);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (half trans.)', ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.no_trans;
        subplot(2,3,4);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', '', ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.prev_trans;
        subplot(2,3,5);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', exps(i).sim_title, ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.half_trans;
        subplot(2,3,6);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', '', ...
            'power-law fit', temp.PL, ...
            'exponential fit to tail', temp.exp_tail, ...
            'plot_on_new_fig', 0);
    end
else
    for i=1:length(exps)
        figure,
        temp = exps(i).bouts.wake.no_trans;
        subplot(2,3,1);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (no trans.)', 'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.wake.prev_trans;
        subplot(2,3,2);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (prev trans.)', 'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.wake.half_trans;
        subplot(2,3,3);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Wake bout (half trans.)', 'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.no_trans;
        subplot(2,3,4);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Sleep bout (no trans.)', 'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.prev_trans;
        subplot(2,3,5);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', exps(i).sim_title, 'plot_on_new_fig', 0);
        
        temp = exps(i).bouts.sleep.half_trans;
        subplot(2,3,6);
        plot_approximate_pdf_of_data(temp.data, ...
            'data_title', 'Sleep bout (half trans.)', 'plot_on_new_fig', 0);
    end
end

end